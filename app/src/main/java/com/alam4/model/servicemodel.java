package com.alam4.model;

public class servicemodel {
    String name,innitial,image,contact,alam,location;

    public servicemodel() {
    }

    public String getContact() {
        return contact;
    }

    public void setContact(String contact) {
        this.contact = contact;
    }

    public String getAlam() {
        return alam;
    }

    public void setAlam(String alam) {
        this.alam = alam;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getInnitial() {
        return innitial;
    }

    public void setInnitial(String innitial) {
        this.innitial = innitial;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}
