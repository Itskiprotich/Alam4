package com.alam4.model;

public class reportmodel {
    String id, groupname, name, level, poster, time_generated, time_neutralized, response_time, safe, shouts, elevates, ignores, shares, neutalized;

    public reportmodel() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getGroupname() {
        return groupname;
    }

    public void setGroupname(String groupname) {
        this.groupname = groupname;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLevel() {
        return level;
    }

    public void setLevel(String level) {
        this.level = level;
    }

    public String getPoster() {
        return poster;
    }

    public void setPoster(String poster) {
        this.poster = poster;
    }

    public String getTime_generated() {
        return time_generated;
    }

    public void setTime_generated(String time_generated) {
        this.time_generated = time_generated;
    }

    public String getTime_neutralized() {
        return time_neutralized;
    }

    public void setTime_neutralized(String time_neutralized) {
        this.time_neutralized = time_neutralized;
    }

    public String getResponse_time() {
        return response_time;
    }

    public void setResponse_time(String response_time) {
        this.response_time = response_time;
    }

    public String getSafe() {
        return safe;
    }

    public void setSafe(String safe) {
        this.safe = safe;
    }

    public String getShouts() {
        return shouts;
    }

    public void setShouts(String shouts) {
        this.shouts = shouts;
    }

    public String getElevates() {
        return elevates;
    }

    public void setElevates(String elevates) {
        this.elevates = elevates;
    }

    public String getIgnores() {
        return ignores;
    }

    public void setIgnores(String ignores) {
        this.ignores = ignores;
    }

    public String getShares() {
        return shares;
    }

    public void setShares(String shares) {
        this.shares = shares;
    }

    public String getNeutalized() {
        return neutalized;
    }

    public void setNeutalized(String neutalized) {
        this.neutalized = neutalized;
    }
}
