package com.alam4.model;

public class ratings {
    String id, members, alertid, safe, shout, elevate,share, ignored, neutralize;

    public ratings() {
    }

    public String getShare() {
        return share;
    }

    public void setShare(String share) {
        this.share = share;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getMembers() {
        return members;
    }

    public void setMembers(String members) {
        this.members = members;
    }

    public String getAlertid() {
        return alertid;
    }

    public void setAlertid(String alertid) {
        this.alertid = alertid;
    }

    public String getSafe() {
        return safe;
    }

    public void setSafe(String safe) {
        this.safe = safe;
    }

    public String getShout() {
        return shout;
    }

    public void setShout(String shout) {
        this.shout = shout;
    }

    public String getElevate() {
        return elevate;
    }

    public void setElevate(String elevate) {
        this.elevate = elevate;
    }

    public String getIgnored() {
        return ignored;
    }

    public void setIgnored(String ignored) {
        this.ignored = ignored;
    }

    public String getNeutralize() {
        return neutralize;
    }

    public void setNeutralize(String neutralize) {
        this.neutralize = neutralize;
    }
}
